	struct Rect {
		union {
			Point pos{};
			struct { int x, y; };
		};
		
		int w{ 1 }, h{ 1 };

		Rect() = default;
		Rect(int x, int y, int w, int h) : x(x), y(y), w(w), h(h) {}

		inline bool contains(Point pt) {
			const int x = pt.x, y = pt.y;
			if (x < this->x || x >= this->w + this->x || y < this->y || y >= this->h + this->y)
				return false;
			return true;
		}

		inline Rect grow(int offset) const {
			return Rect(
				x - offset,
				y - offset,
				w + offset * 2,
				h + offset * 2
			);
		}
	};

